#include<bits/stdc++.h>
using namespace std;

int main(){
    int n;
    cin >> n;

    // Linearithmic compexity O(nlogn)

    for (int i = 1; i <= n; i++)
    {
        for (int j = 0; j < n; j*=2)
        {
            cout << "Huda" << endl;
        }
        
    }
    
    return 0;
}